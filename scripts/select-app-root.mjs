#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { DEFAULT_APP_ID, PROJECT_ROOT, SUPPORTED_APP_IDS, getAppConfig } from './app-registry.mjs';

const root = PROJECT_ROOT;
const appId = process.env.APP_ID || process.argv[2] || DEFAULT_APP_ID;
const appConfig = getAppConfig(appId);

const routerRoot = appConfig.routerRoot ?? null;
const generatedDir = path.join(root, '.generated');
fs.mkdirSync(generatedDir, { recursive: true });

if (routerRoot) {
  const routerRootPath = path.join(root, routerRoot);
  if (!fs.existsSync(routerRootPath)) {
    throw new Error(`Router root not found: ${routerRootPath}`);
  }

  const envFile = path.join(generatedDir, 'app-selection.env');
  fs.writeFileSync(
    envFile,
    [
      `export APP_ID='${appId}'`,
      `export EXPO_ROUTER_APP_ROOT='${routerRoot}'`,
      `export EXPO_PUBLIC_APP_ID='${appId}'`,
    ].join('\n') + '\n'
  );

  const buildConfigPath = path.join(root, 'src', 'generated', 'build-app-config.ts');
  fs.writeFileSync(
    buildConfigPath,
    [
      '// AUTO-GENERATED by scripts/select-app-root.mjs',
      `export const BUILD_APP_ID = '${appId}' as const;`,
      `export const BUILD_APP_DISPLAY_NAME = '${appConfig.displayName}' as const;`,
      `export const BUILD_ROUTER_ROOT = '${routerRoot}' as const;`,
      `export const BUILD_APP_ROUTE_BASE = '${appConfig.routeBase}' as const;`,
      `export const BUILD_APP_STORAGE_NAMESPACE = '${appConfig.storageNamespace}' as const;`,
      `export const BUILD_SUPPORTED_APP_IDS = ${JSON.stringify(SUPPORTED_APP_IDS)} as const;`,
    ].join('\n') + '\n'
  );

  console.log(`[select-app-root] APP_ID=${appId} EXPO_ROUTER_APP_ROOT=${routerRoot}`);
} else {
  // Backend-only app (frontend lives in its own repo). Write minimal env.
  const envFile = path.join(generatedDir, 'app-selection.env');
  fs.writeFileSync(
    envFile,
    [
      `export APP_ID='${appId}'`,
      `export EXPO_PUBLIC_APP_ID='${appId}'`,
    ].join('\n') + '\n'
  );
  console.log(`[select-app-root] APP_ID=${appId} (backend-only, no router root)`);
}
